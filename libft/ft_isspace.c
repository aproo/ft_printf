#include "libft.h"

int		ft_isspace(char c)
{
	if (c == ' ')
		return (1);
	if (c == '\t')
		return (2);
	if (c == '\v')
		return (3);
	if (c == '\n')
		return (4);
	if (c == '\f')
		return (5);
	if (c == '\r')
		return (6);
	return (0);
}
