#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ch;

	ch = NULL;
	while (*s)
	{
		if (*s != c)
			s++;
		if (*s == c)
		{
			ch = (char *)s;
			if (*s != '\0')
				s++;
		}
	}
	return (ch);
}
